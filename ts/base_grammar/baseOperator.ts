// 基本运算符操作

var num1:number = 10
var num2:number =2
var res:number = 0

res = num1 + num2
console.log(" 加的结果是 : " + res);

res = num1 - num2
console.log(" num1 - num2 的结果是 : " + res)

res = num1 * num2
console.log(" num1*num2 的结果是 : " + res)

res = num1 / num2
console.log(" num1 / num2 的结果是 : " + res)

res = num1 % num2
console.log(" num1 % num2 的结果是 : " + res)

num1 ++
console.log(" num1 ++ 自增运算符是 : " + num1)

num2 --
console.log(" num2 -- 自减运算符是 : " + num2)
