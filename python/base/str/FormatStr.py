'''
    格式化字符串类型
'''

age = 18
name = 'TOM'
weight = 75.5
stu_id = 1
stu_id = 1000

# 格式化数字
print('我的名字是%d岁' %age)

# 格式化字符串
print('我的名字是%s'%name)

# 格式化小数点类型的, 2f 中的2是保留小数点的位数
print('我的体重是%.2f公斤'%weight)

# 格式化
print('我的学号是%03d'%stu_id)


